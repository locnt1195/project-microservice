import com.google.api.services.drive.Drive.Files.Get;
import com.google.api.services.drive.DriveScopes;
import com.google.api.services.drive.model.File;
import com.google.api.services.drive.model.FileList;
import com.google.api.services.drive.model.Permission;

@RestController
public class UploadFileController {
	@Autowired
    UploadFileRepository uploadFileRepository;
	
	@Autowired
    IVideoRepository videoRepository;
 
    List<String> files = new ArrayList<String>();
    
    @RequestMapping(value = "/uploadFile", method = RequestMethod.POST)
    public @ResponseBody boolean uploadFile(@RequestParam(value="file") MultipartFile file) throws IOException {
        boolean uploadResult = uploadFileRepository.store(file);
        if(!uploadResult)
        	return false;
        
        Drive driveService = getDriveService();
        
        File fileMetadata = new File();
        fileMetadata.setName(file.getOriginalFilename());
        
        List<Permission> permissions = new ArrayList<Permission>();
        permissions.add(new Permission().setType("anyone").setRole("reader"));   

        java.io.File filePath = new java.io.File("upload-dir/" + file.getOriginalFilename());
        FileContent mediaContent = new FileContent(file.getContentType(), filePath);
        File f = driveService.files().create(fileMetadata, mediaContent)
                .setFields("id")
                .execute();
        
        JsonBatchCallback<Permission> callback = new JsonBatchCallback<Permission>() {
            @Override
            public void onFailure(GoogleJsonError e,
                                  HttpHeaders responseHeaders)
                    throws IOException {
                // Handle error
                System.err.println(e.getMessage());
            }

            @Override
            public void onSuccess(Permission permission,
                                  HttpHeaders responseHeaders)
                    throws IOException {
                System.out.println("Permission ID: " + permission.getId());
            }
        };
        
        BatchRequest batch = driveService.batch();
        driveService.permissions().create(f.getId(), new Permission().setType("anyone").setRole("reader").setAllowFileDiscovery(true))
        .setFields("id")
        .queue(batch, callback);
        
        java.io.File deleteFile = new java.io.File("upload-dir/" + file.getOriginalFilename());
        if(deleteFile != null)
        	deleteFile.delete();
        batch.execute();
        
        String shareableLink = "https://drive.google.com/file/d/" + f.getId() + "/view?usp=sharing";
        System.out.println(shareableLink);
        
        Video video = new Video(file.getOriginalFilename(), shareableLink, "VideoType");
        videoRepository.save(video);
        
        return true;
    }
    
    /** Application name. */
    private static final String APPLICATION_NAME =
        "Drive API Upload Service";

    /** Directory to store user credentials for this application. */
    private static final java.io.File DATA_STORE_DIR = new java.io.File(
        System.getProperty("user.home"), ".credentials/drive-upload-file-service");

    /** Global instance of the {@link FileDataStoreFactory}. */
    private static FileDataStoreFactory DATA_STORE_FACTORY;

    /** Global instance of the JSON factory. */
    private static final JsonFactory JSON_FACTORY =
        JacksonFactory.getDefaultInstance();

    /** Global instance of the HTTP transport. */
    private static HttpTransport HTTP_TRANSPORT;

    /** Global instance of the scopes required by this quickstart.
     *
     * If modifying these scopes, delete your previously saved credentials
     * at ~/.credentials/drive-java-quickstart
     */
    private static final List<String> SCOPES =
        Arrays.asList(DriveScopes.DRIVE);

    static {
        try {
            HTTP_TRANSPORT = GoogleNetHttpTransport.newTrustedTransport();
            DATA_STORE_FACTORY = new FileDataStoreFactory(DATA_STORE_DIR);
        } catch (Throwable t) {
            t.printStackTrace();
            System.exit(1);
        }
    }

    /**
     * Creates an authorized Credential object.
     * @return an authorized Credential object.
     * @throws IOException
     */
    public static Credential authorize() throws IOException {
        // Load client secrets.
        InputStream in =
            UploadFileController.class.getResourceAsStream("/client_secret.json");
        GoogleClientSecrets clientSecrets =
            GoogleClientSecrets.load(JSON_FACTORY, new InputStreamReader(in));

        // Build flow and trigger user authorization request.
        GoogleAuthorizationCodeFlow flow =
                new GoogleAuthorizationCodeFlow.Builder(
                        HTTP_TRANSPORT, JSON_FACTORY, clientSecrets, SCOPES)
                .setDataStoreFactory(DATA_STORE_FACTORY)
                .setAccessType("offline")
                .build();
        Credential credential = new AuthorizationCodeInstalledApp(
            flow, new LocalServerReceiver()).authorize("user");
        System.out.println(
                "Credentials saved to " + DATA_STORE_DIR.getAbsolutePath());
        return credential;
    }

    /**
     * Build and return an authorized Drive client service.
     * @return an authorized Drive client service
     * @throws IOException
     */
    public static Drive getDriveService() throws IOException {
        Credential credential = authorize();
        return new Drive.Builder(
                HTTP_TRANSPORT, JSON_FACTORY, credential)
                .setApplicationName(APPLICATION_NAME)
                .build();
    }
}
